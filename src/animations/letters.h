void add_letter_to_matrix(uint8_t letter[7][6], uint8_t* output, int output_column_offset) {
    int output_columns = sizeof output / 7; 
    for (int row = 0; row < 7; row++) {
        int input_start = (row * 7);
        int num_bytes = (output_column_offset + 7) < output_columns ? 7 : (output_columns - output_column_offset);

        memcpy(output, letter + input_start, num_bytes);
    }
}

